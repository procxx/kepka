Data PrepareData() {
	Data result;

	auto variatedIds = fillVariatedIds();
	if (variatedIds.empty()) {
		return Data();
	}

	// Manually add :speech_left: emoji before eye-with-speech emoji.
	if (!AddItemBeforeItem({ 0xD83DDDE8U }, { 0xD83DDC41U, 0x200DU, 0xD83DDDE8U })) {
		return Data();
	}

	for (auto category : {
		&Category1,
		&Category2,
		&Category3,
		&Category4,
		&Category5,
		&Category6,
		&Category7,
	}) {
		appendCategory(result, *category, variatedIds);
		if (result.list.empty()) {
			return Data();
		}
	}

	fillReplaces(result);
	if (result.list.empty()) {
		return Data();
	}

	return result;
}

